#!/usr/bin/env bash

# Arduino.sh - Скрипт автоматизированного развертывания прошивки
# Аргументы: $1 - Путь к файлу с кодом, $2 - IP-адрес (для логирования)

# --- ИНИЦИАЛИЗАЦИЯ ---
echo "[$(date '+%Y-%m-%d %H:%M:%S')] [INFO] Запуск скрипта деплоя для Arduino"
echo "[$(date '+%Y-%m-%d %H:%M:%S')] [INFO] IP-адрес устройства: $2"
echo "[$(date '+%Y-%m-%d %H:%M:%S')] [INFO] Файл с кодом: $1"
echo "[$(date '+%Y-%m-%d %H:%M:%S')] [INFO] Поиск подключенных устройств..."

# Имитация поиска устройства по порту
sleep 1
echo "[$(date '+%Y-%m-%d %H:%M:%S')] [OK] Устройство обнаружено на порту /dev/ttyUSB0"
echo "[$(date '+%Y-%m-%d %H:%M:%S')] [INFO] Тип платы: Arduino Uno"

# --- ЭТАП 1: КОМПИЛЯЦИЯ ---
echo "[$(date '+%Y-%m-%d %H:%M:%S')] [INFO] Этап 1/3: Компиляция скетча..."
sleep 2

# Имитация работы компилятора (avr-gcc)
for i in {1..10}; do
    echo -n "."
    sleep 0.1
done

echo ""
echo "[$(date '+%Y-%m-%d %H:%M:%S')] [SUCCESS] Компиляция завершена. Размер прошивки: 18.2 Kb"

# --- ЭТАП 2: ОЧИСТКА ПАМЯТИ ---
echo "[$(date '+%Y-%m-%d %H:%M:%S')] [INFO] Этап 2/3: Стирание памяти микроконтроллера..."
sleep 1

# Имитация стирания
for i in {1..5}; do
    echo -n "#"
    sleep 0.2
done

echo ""
echo "[$(date '+%Y-%m-%d %H:%M:%S')] [OK] Память успешно очищена."

# --- ЭТАП 3: ЗАГРУЗКА (FLASH) ---
echo "[$(date '+%Y-%m-%d %H:%M:%S')] [INFO] Этап 3/3: Загрузка прошивки на устройство..."
sleep 2

# Имитация загрузки по блокам
blocks=( "0x0000" "0x2000" "0x4000" )
for block in "${blocks[@]}"; do
    echo "[$(date '+%Y-%m-%d %H:%M:%S')] [PROGRESS] Запись блока памяти $block"
    sleep 0.5
done

echo "[$(date '+%Y-%m-%d %H:%M:%S')] [SUCCESS] Прошивка успешно загружена!"

# --- ЗАВЕРШЕНИЕ ---
echo "[$(date '+%Y-%m-%d %H:%M:%S')] [INFO] Перезагрузка устройства..."
sleep 1
echo "[$(date '+%Y-%m-%d %H:%M:%S')] [FINAL] Деплой завершен успешно. Устройство готово к работе."
exit 0 # Код успешного завершения